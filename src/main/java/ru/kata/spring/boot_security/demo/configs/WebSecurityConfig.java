    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }

    @Bean
    public DaoAuthenticationProvider daoAuthenticationProvider() {
        DaoAuthenticationProvider daoAProvider = new DaoAuthenticationProvider();
        daoAProvider.setPasswordEncoder(passwordEncoder());
        daoAProvider.setUserDetailsService(userServiceImpl);
        return daoAProvider;
    }
